"""Base interfaces for institutional portfolio models."""

from __future__ import annotations

from abc import ABC, abstractmethod
from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional

import numpy as np
import pandas as pd


# ----------------------------------------------------

@dataclass
class TargetAllocation:
    """Target portfolio weights from a model."""
    weights: Dict[str, float]  # symbol -> weight (-1..1, negative = short)
    regime: str = ""           # current regime label (if applicable)
    signal_strength: float = 1.0
    meta: Dict[str, Any] = field(default_factory=dict)

    @property
    def gross_exposure(self) -> float:
        return sum(abs(w) for w in self.weights.values())

    @property
    def net_exposure(self) -> float:
        return sum(self.weights.values())

    @property
    def symbols(self) -> List[str]:
        return list(self.weights.keys())


@dataclass
class RebalanceOrder:
    """Order generated by rebalance logic."""
    symbol: str
    side: str       # buy / sell
    qty: float      # in base units
    weight: float   # target weight
    reason: str = ""


# ----------------------------------------------------

@dataclass
class PortfolioState:
    """Multi-asset portfolio snapshot."""
    equity: float = 10000.0
    cash: float = 10000.0
    positions: Dict[str, float] = field(default_factory=dict)     # symbol -> qty
    avg_entries: Dict[str, float] = field(default_factory=dict)   # symbol -> avg price
    weights: Dict[str, float] = field(default_factory=dict)       # symbol -> current weight
    unrealized_pnl: float = 0.0
    realized_pnl: float = 0.0
    total_trades: int = 0
    max_equity: float = 10000.0
    max_drawdown: float = 0.0

    def position_value(self, symbol: str, price: float) -> float:
        return self.positions.get(symbol, 0.0) * price

    def gross_exposure(self, prices: Dict[str, float]) -> float:
        return sum(abs(self.positions.get(s, 0) * p) for s, p in prices.items())


# ----------------------------------------------------

class PortfolioModel(ABC):
    """Base class for all institutional model families."""

    name: str = "base_model"
    group: str = "quant"

    def __init__(self, cfg: Dict[str, Any]):
        self.cfg = cfg
        self.params = cfg.get("params", {})
        self.universe: List[str] = cfg.get("universe", [])

    @abstractmethod
    def fit(self, data: Dict[str, pd.DataFrame]) -> None:
        """Fit model on historical data. data = {symbol: ohlcv_df}."""
        pass

    @abstractmethod
    def compute_targets(self, data: Dict[str, pd.DataFrame]) -> TargetAllocation:
        """Compute target weights given current data window."""
        pass

    def rebalance(
        self,
        state: PortfolioState,
        prices: Dict[str, float],
        targets: TargetAllocation,
    ) -> List[RebalanceOrder]:
        """Convert target weights into orders to execute.
        Default implementation: compute weight delta -> orders.
        """
        orders = []
        equity = state.equity

        for symbol, target_w in targets.weights.items():
            price = prices.get(symbol, 0.0)
            if price <= 0:
                continue

            # Current weight
            current_qty = state.positions.get(symbol, 0.0)
            current_value = current_qty * price
            current_w = current_value / equity if equity > 0 else 0.0

            # Delta
            delta_w = target_w - current_w
            delta_value = delta_w * equity
            delta_qty = delta_value / price

            # Minimum order threshold (0.1% of equity)
            if abs(delta_value) < equity * 0.001:
                continue

            if delta_qty > 0:
                orders.append(RebalanceOrder(
                    symbol=symbol, side="buy", qty=abs(delta_qty),
                    weight=target_w, reason=targets.meta.get("reason", "rebalance"),
                ))
            elif delta_qty < 0:
                orders.append(RebalanceOrder(
                    symbol=symbol, side="sell", qty=abs(delta_qty),
                    weight=target_w, reason=targets.meta.get("reason", "rebalance"),
                ))

        return orders


