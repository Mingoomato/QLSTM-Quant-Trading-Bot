class RebalanceScheduler:
    """Determines when to rebalance based on frequency config."""

    def __init__(self, frequency: str = "weekly"):
        self.frequency = frequency
        self._last_rebalance_idx: int = -999
        self._bar_count: int = 0

    def should_rebalance(self, idx: int, ts: Optional[pd.Timestamp] = None) -> bool:
        """Check if we should rebalance at this bar index."""
        self._bar_count = idx

        if self.frequency == "daily":
            period = 1
        elif self.frequency == "weekly":
            period = 5
        elif self.frequency == "monthly":
            period = 21
        elif self.frequency == "biweekly":
            period = 10
        else:
            period = int(self.frequency) if self.frequency.isdigit() else 5

        if idx - self._last_rebalance_idx >= period:
            self._last_rebalance_idx = idx
            return True
        return False

    def bars_until_next(self) -> int:
        if self.frequency == "daily":
            period = 1
        elif self.frequency == "weekly":
            period = 5
        elif self.frequency == "monthly":
            period = 21
        elif self.frequency == "biweekly":
            period = 10
        else:
            period = int(self.frequency) if self.frequency.isdigit() else 5
        return max(0, period - (self._bar_count - self._last_rebalance_idx))


